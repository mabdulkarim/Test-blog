    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StorePostRequest $request)
    {
        $post = Post::create($request->safe()->merge(['user_id' => auth()->user()->id])->except('image'));

        if ($request->hasFile('image')) {
            $post->update([
                'image_path' => Storage::disk('public')->putFile('posts', $request->file('image'))
            ]);
        }

        return redirect()->route('home')->with('success', 'Post was successfully created!');
    }

